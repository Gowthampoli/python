languages = ["Python1", "Java", "C++", "JavaScript", "Ruby"]

# Check if "Python" is present using 'in' operator
if "Python" in languages:
    print("Python is present in the list.")
else:
    print("Python is not present in the list.")

# Check if "Python" is present using 'not in' operator
if "Python" not in languages:
    print("Python is not present in the list.")
else:
    print("Python is present in the list.")
